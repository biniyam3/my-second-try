package ifElse;

public class IfElse3 {

	public static void main(String[] args) {

		int a = 50;
		int b = 60;
		
		if (a>b) {
			System.out.println("a is larger than b");
		}
		else if (a==b) {
			System.out.println("a is equal to b");
		}
		else {
			System.out.println("b is larger than a");
		}
		
	}

}
